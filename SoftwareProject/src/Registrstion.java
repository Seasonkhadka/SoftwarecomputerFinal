    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name =jname.getText();
        String username =juname.getText();
        String Email =jemail.getText();
        String Password =jpass.getText();
        String conPassword =jconpass.getText();
        try {
            Statement s =db.mycon().createStatement();
            s.executeUpdate("INSERT INTO table_register(Name,Username,Email,Password) VALUES ('"+name+"','"+username+"','"+Email+"','"+Password+"')");
            if (Password.equals(conPassword)) {
    // Passwords match, proceed to create the account and show success message
         JOptionPane.showMessageDialog(rootPane, "Your Account is Created. Please Login Now.");
         this.setVisible(false);
            new LoginForm().setVisible(true);
} else {
    // Passwords don't match, show an error message
    JOptionPane.showMessageDialog(rootPane, "Error: Passwords do not match. Please try again.");
    // Optionally, you can clear the password fields here for the user to re-enter
    jpass.setText("");
    jconpass.setText("");
}
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

